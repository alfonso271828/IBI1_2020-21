def percentage(x,y):
    count = 0
    for i in range(len(x)):
        if y[i]!=x[i]:
            count = count+1# match two sequence and count the number of different amino acids
    percentage_t=count/len(x)
    percentage= 1 - percentage_t
    return percentage

